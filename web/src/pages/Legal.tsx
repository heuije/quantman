import { Link, useNavigate, useParams } from "react-router-dom";

type Section = "terms" | "privacy" | "usage";

const TITLE: Record<Section, string> = {
  terms: "서비스 이용약관",
  privacy: "개인정보처리방침",
  usage: "서비스 이용안내",
};

export default function Legal() {
  const { section } = useParams<{ section: Section }>();
  const nav = useNavigate();
  const cur: Section = (section && section in TITLE ? section : "terms") as Section;

  return (
    <div className="legal-wrap">
      <header className="legal-header">
        <button className="ghost sm" onClick={() => nav(-1)}>← 뒤로</button>
        <div className="brand" style={{ padding: 0 }}>
          퀀트<span>플랫폼</span>
        </div>
      </header>

      <nav className="legal-nav">
        {(Object.keys(TITLE) as Section[]).map((s) => (
          <Link key={s} to={`/legal/${s}`}
                className={"legal-tab" + (cur === s ? " on" : "")}>
            {TITLE[s]}
          </Link>
        ))}
      </nav>

      <article className="legal-content">
        <div className="legal-template-banner">
          ⚠ 본 문서는 한국 자동매매 SaaS 가이드라인 기반 <b>표준 템플릿</b>이며,
          실서비스 운영 전 자본시장법 전문 로펀의 재검수가 전제됩니다. 본 코드를
          그대로 상용 배포 시 법적 책임은 운영자에게 귀속됩니다.
        </div>
        {cur === "terms" && <TermsContent />}
        {cur === "privacy" && <PrivacyContent />}
        {cur === "usage" && <UsageContent />}
      </article>
    </div>
  );
}

